onMounted(()=>{
  //懒加载
  const ob=new IntersectionObserver(
  (entries)=>{
 for(const entry of entries){
  if(entry.isIntersecting){
   const img=entry.target;
   img.src=img.dataset.src;
   ob.unobserve(img);
  }}
},
 {
  threshold:0
 })
